
package org.top.java.netty.source.util.internal.logging;

/**
 * The log level that {@link InternalLogger} can log at.
 */

/**
 * {@link InternalLogger} 可以记录的日志级别。
 */
public enum InternalLogLevel {
    /**
     * 'TRACE' log level.
     */
    /**
     * 'TRACE' 日志级别。
     */
    TRACE,
    /**
     * 'DEBUG' log level.
     */
    /**
     * 'DEBUG' 日志级别。
     */
    DEBUG,
    /**
     * 'INFO' log level.
     */
    /**
     * 'INFO' 日志级别。
     */
    INFO,
    /**
     * 'WARN' log level.
     */
    /**
     * 'WARN' 日志级别。
     */
    WARN,
    /**
     * 'ERROR' log level.
     */
    /**
     * 'ERROR' 日志级别。
     */
    ERROR
}
